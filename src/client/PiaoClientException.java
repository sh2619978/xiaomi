package client;

public class PiaoClientException extends Exception {

    public PiaoClientException() {
        super();
    }

    public PiaoClientException(String message, Throwable cause) {
        super(message, cause);
    }

    public PiaoClientException(String message) {
        super(message);
    }

    public PiaoClientException(Throwable cause) {
        super(cause);
    }
    
}
